 /*01. What are the details of all cars purchased in the year 2022?*/

 SELECT
	sales.car_id,
	purchase_date,
	make,
	type,
	style,
	cost_$ 
 FROM 
	sales
 JOIN
	cars ON cars.car_id = sales.car_id
 WHERE
	YEAR(sales.purchase_date) ='2022'
 ORDER BY
	purchase_date DESC;

 /*02. What is the total number of cars sold by each salesperson?*/

 SELECT 
	salespersons.salesman_id,
	name,
	COUNT(sales.sale_id) AS total_no_of_cars_sold
 FROM 
	salespersons
 JOIN 
	sales ON sales.salesman_id = salespersons.salesman_id
 GROUP BY
	salespersons.salesman_id,
	name
 ORDER BY 
	total_no_of_cars_sold DESC;

 /*03. What is the total revenue generated by each salesperson?*/

 SELECT 
	salespersons.salesman_id,
	name,
	SUM(cost_$) AS total_revenue_generated
 FROM 
	salespersons
 JOIN 
	sales
		ON sales.salesman_id = salespersons.salesman_id
 JOIN
	cars
		ON cars.car_id = sales.car_id
 GROUP BY
	salespersons.salesman_id,
	name
 ORDER BY 
	total_revenue_generated DESC;

 /*04. What are the details of the cars sold by each salesperson? */

 SELECT
	cars.car_id,
	make,
	type,
	style,
	cost_$,
	purchase_date,
	name AS salesman
 FROM
	cars
 JOIN
	sales ON cars.car_id = sales.car_id
 JOIN
	salespersons ON salespersons.salesman_id =sales.salesman_id
 ORDER BY car_id;

 /*05. What is the total revenue generated by each car type?*/

 SELECT 
	type AS car_type,
	SUM(cost_$) AS total_revenue_generated
 FROM
	cars
 GROUP BY
	type
 ORDER BY total_revenue_generated DESC;

