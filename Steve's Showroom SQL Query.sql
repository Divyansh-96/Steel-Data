/*09. What is the name and city of the salesperson who sold the most number of cars in the year 2023?*/

 SELECT TOP 1
	name,
	city,
	COUNT(name) AS number_of_cars_sold
 FROM
	salespersons
 JOIN sales ON sales.salesman_id = salespersons.salesman_id
 WHERE 
	YEAR(purchase_date) = '2023'
 GROUP BY
	name, city
 ORDER BY
	number_of_cars_sold DESC;

 /*10. What is the name and age of the salesperson who generated the highest revenue in the year 2022? */

 SELECT TOP 1
	name,
	age,
	SUM(cost_$) AS highest_revenue
 FROM
	salespersons
 JOIN sales ON sales.salesman_id = salespersons.salesman_id
 JOIN cars ON sales.car_id = cars.car_id
 WHERE
	YEAR(purchase_date) ='2022'
 GROUP BY
	name, age
 ORDER BY highest_revenue DESC;
